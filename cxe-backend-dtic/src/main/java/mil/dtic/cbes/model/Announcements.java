package mil.dtic.cbes.model;

public class Announcements {
	
	private Integer idAnnouncements;
	
	private String p40_title;
	 
	private String p40_header;
	
	private String p40_text;
	
	private String r2_title;
	 
	private String r2_header;
	
	private String r2_text;

	public Integer getIdAnnouncements() {
		return idAnnouncements;
	}

	public void setIdAnnouncements(Integer idAnnouncements) {
		this.idAnnouncements = idAnnouncements;
	}

	public String getP40_title() {
		return p40_title;
	}

	public void setP40_title(String p40_title) {
		this.p40_title = p40_title;
	}

	public String getP40_header() {
		return p40_header;
	}

	public void setP40_header(String p40_header) {
		this.p40_header = p40_header;
	}

	public String getR2_title() {
		return r2_title;
	}

	public void setR2_title(String r2_title) {
		this.r2_title = r2_title;
	}

	public String getR2_header() {
		return r2_header;
	}

	public void setR2_header(String r2_header) {
		this.r2_header = r2_header;
	}

	public String getP40_text() {
		return p40_text;
	}

	public void setP40_text(String p40_text) {
		this.p40_text = p40_text;
	}

	public String getR2_text() {
		return r2_text;
	}

	public void setR2_text(String r2_text) {
		this.r2_text = r2_text;
	}
}
